void	*memchr(const void *s, int c, size_t n)
{
	char	*src;
	int	i;

	src = (char *)s;
	i = 0;
	while (i < n)
	{
		if (src[i] == c)
			return (src[i])
		i++;
	}
	return (NULL);
}
